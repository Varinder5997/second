package firstTest;

public class Team {
	private int teamId;
	private String teamTrack;
	private Lead Lead;
	public int getTeamId() {
		return teamId;
	}
	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}
	public String getTeamTrack() {
		return teamTrack;
	}
	public void setTeamTrack(String teamTrack) {
		this.teamTrack = teamTrack;
	}
	public Lead getLead() {
		return Lead;
	}
	public void setLead(Lead lead) {
		Lead = lead;
	}
	
}
