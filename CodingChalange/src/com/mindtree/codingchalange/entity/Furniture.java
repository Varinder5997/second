package com.mindtree.codingchalange.entity;

public class Furniture {
	private int furnitureId;
	private String furnitureName;
	private double furnitureCost;
	private int furnitureQuentity;
	public int getFurnitureId() {
		return furnitureId;
	}
	public void setFurnitureId(int furnitureId) {
		this.furnitureId = furnitureId;
	}
	public String getFurnitureName() {
		return furnitureName;
	}
	public void setFurnitureName(String furnitureName) {
		this.furnitureName = furnitureName;
	}
	public double getFurnitureCost() {
		return furnitureCost;
	}
	public void setFurnitureCost(double furnitureCost) {
		this.furnitureCost = furnitureCost;
	}
	public int getFurnitureQuentity() {
		return furnitureQuentity;
	}
	public void setFurnitureQuentity(int furnitureQuentity) {
		this.furnitureQuentity = furnitureQuentity;
	}
	
}
